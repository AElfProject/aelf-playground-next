export function FileIcon({ filename }: { filename: string }) {
  if (filename.endsWith("cs")) return <CSIcon />;
  if (filename.endsWith("csproj")) return <CSProjIcon className="h-4 w-4" />;
  if (filename.endsWith("proto")) return <ProtobufIcon className="h-4 w-4" />;
  if (typescriptExtensions.some((i) => filename.endsWith(i)))
    return <TSIcon className="h-4 w-4" />;
  if (filename.endsWith("css")) return <CSSIcon className="h-4 w-4" />;
  if (imageExtensions.some((i) => filename.endsWith(i)))
    return <ImageIcon className="h-4 w-4" />;
  if (filename.includes("tsconfig"))
    return <TSConfigIcon className="h-4 w-4" />;
  if (javascriptExtensions.some((i) => filename.endsWith(i)))
    return <JSIcon className="h-4 w-4" />;
  if (htmlExtensions.some((i) => filename.endsWith(i)))
    return <HTMLIcon className="h-4 w-4" />;
  if (markdownExtensions.some((i) => filename.endsWith(i)))
    return <MarkdownIcon className="h-4 w-4" />;
  if (filename.endsWith("gitignore")) return <GitIcon className="h-4 w-4" />;
  if (filename.endsWith("json")) return <JSONIcon className="h-4 w-4" />;

  return <File className="h-4 w-4" />;
}

